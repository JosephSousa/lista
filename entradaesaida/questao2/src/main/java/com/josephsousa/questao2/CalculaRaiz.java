/**
 * 
 * 
 * classe responsável por efetuar o cálculo da raíz quadrada de um número.
 */


public class CalculaRaiz {
      /**Método responsável por calcular a raiz quadrada de um número
     * 
     * @param numero - número recebido para ser calculada sua raíz quadrada 
     * @return - este método retorna o resultado da multiplicação de dois números
     */ 
    public double calcularRaizQuadrada(double numero){
        return Math.sqrt(numero);
    }
}
