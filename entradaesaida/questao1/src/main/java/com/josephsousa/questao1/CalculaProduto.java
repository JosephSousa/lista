/**
 * 
 * 
 * classe responsável por efetuar o cálculo do produto de dois números.
 */
public class CalculaProduto {
    /**Método responsável por calcular o produto de dois números
     * 
     * @param termo1 - este parâmetro é um número real recebido para ser efetuada a multiplicação
     * @param termo2 - este parâmetro é um número real recebido para ser efetuada a multiplicação
     * @return - este método retorna o resultado da multiplicação de dois números
     */ 
    public double calcularProduto(double termo1, double termo2){
        return termo1*termo2;
    }
    
}
